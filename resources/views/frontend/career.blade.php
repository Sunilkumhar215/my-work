    let x = (window.innerWidth / 2 - e.pageX) / 35;
    let y = (window.innerHeight / 2 - e.pageY) / 35;

    hero.style.transform = `rotateY(${x}deg) rotateX(${y}deg)`;
});

hero.addEventListener("mouseleave", () => {
    hero.style.transform = "rotateY(0deg) rotateX(0deg)";
});
</script>

